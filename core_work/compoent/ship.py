import numpy as np
import pyvista as pv
# 定义顶点
vertices = [
    # 底部的矩形
    [0, 0, 0],    # 顶点 0
    [1, 0, 0],    # 顶点 1
    [1, 2, 0],    # 顶点 2
    [0, 2, 0],    # 顶点 3
    
    # 顶部的矩形
    [0, 0, 0.5],  # 顶点 4
    [1, 0, 0.5],  # 顶点 5
    [1, 2, 0.5],  # 顶点 6
    [0, 2, 0.5],  # 顶点 7
    
    # 船头
    [0.5, 2, 0.5],  # 顶点 8
    [0.5, 2.5, 1],  # 顶点 9
    
    # 船尾
    [0.5, 0, 0.5],  # 顶点 10
    [0.5, -0.5, 1], # 顶点 11
    
    # 帆
    [0.5, 1, 1.5],  # 顶点 12
    [0.5, 1.5, 2],  # 顶点 13
    [0.5, 1, 2],    # 顶点 14
    
    # 帆的杆子
    [0.5, 1, 0.5],  # 顶点 15
    [0.5, 1, 1.5],  # 顶点 16
    [0.5, 1.1, 0.5],  # 顶点 17
    [0.5, 1.1, 1.5],  # 顶点 18
]

# 定义面
faces = [
    # 底部的矩形
    4, 0, 1, 2, 3,
    
    # 顶部的矩形
    4, 4, 5, 6, 7,
    
    # 船头
    3, 2, 3, 8,
    3, 3, 8, 9,
    3, 8, 9, 2,
    
    # 船尾
    3, 0, 1, 10,
    3, 1, 10, 11,
    3, 10, 11, 0,
    
    # 帆
    3, 12, 13, 14,
    
    # 船体的厚度
    4, 0, 1, 5, 4,
    4, 1, 2, 6, 5,
    4, 2, 3, 7, 6,
    4, 3, 0, 4, 7,
    
    # 船头和船尾的厚度
    3, 2, 8, 6,
    3, 8, 6, 7,
    3, 0, 10, 4,
    3, 10, 4, 7,
    
    # 帆的杆子
    4, 15, 16, 18, 17,
    4, 15, 17, 18, 16,
]

# 创建 PolyData 网格
mesh = pv.PolyData(vertices, faces)

# 创建 Plotter 对象
plotter = pv.Plotter()

# 添加网格到 Plotter
plotter.add_mesh(mesh, show_edges=True, color='blue', opacity=0.8)

# 设置背景颜色
plotter.background_color = 'white'

# 显示图形
plotter.show()